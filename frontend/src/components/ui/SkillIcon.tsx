// ─── Category ID → Color mapping ─────────────────────────────────────────────

const CATEGORY_COLOR_MAP: Record<string, IconColorTheme> = {
  languages: 'primary',
  frontend: 'secondary',
  backend: 'accent',
  cloud: 'secondary',
  databases: 'primary',
  tools: 'neutral',
}

export function getCategoryColor(categoryId?: string): IconColorTheme {
  if (!categoryId) return 'primary'
  return CATEGORY_COLOR_MAP[categoryId] ?? 'primary'
}

// ─── SkillIcon Component ─────────────────────────────────────────────────────

interface SkillIconProps {
  name: string
  size?: number
  colorTheme?: IconColorTheme
  /** Wrap in a glass orb with glow */
  orb?: boolean
  /** Extra classes on the outermost element */
  className?: string
}

export function SkillIcon({
  name,
  size = 20,
  colorTheme = 'primary',
  orb = false,
  className,
}: SkillIconProps) {
  const gradientId = useId()
  const icon = ICON_MAP[name]
  const colors = COLOR_CONFIG[colorTheme]

  // Fallback for unmapped icons
  if (!icon) {
    if (orb) {
      return (
        <span
          className={cn(
            'inline-flex items-center justify-center rounded-xl',
            'border backdrop-blur-sm transition-all duration-300',
            colors.bg,
            colors.border,
            colors.glow,
            className
          )}
          style={{ width: size + 16, height: size + 16 }}
        >
          <Code size={size} className="text-[var(--text-tertiary)]" />
        </span>
      )
    }
    return <Code size={size} className={cn('text-[var(--text-tertiary)]', className)} />
  }

  const svgEl = (
    <svg
      width={size}
      height={size}
      viewBox={icon.viewBox ?? '0 0 24 24'}
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      aria-hidden="true"
      className="relative z-10 flex-shrink-0"
    >
      <defs>
        <linearGradient id={gradientId} x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" stopColor={colors.from} />
          <stop offset="100%" stopColor={colors.to} />
        </linearGradient>
      </defs>
      <g fill={`url(#${gradientId})`}>{icon.paths}</g>
    </svg>
  )

  if (!orb) {
    return <span className={cn('inline-flex flex-shrink-0', className)}>{svgEl}</span>
  }

  return (
    <span
      className={cn(
        'inline-flex items-center justify-center rounded-xl',
        'border backdrop-blur-sm',
        'transition-all duration-300',
        'group-hover:scale-110',
        colors.bg,
        colors.border,
        colors.glow,
        className
      )}
      style={{ width: size + 16, height: size + 16 }}
    >
      {svgEl}
    </span>
  )
}

